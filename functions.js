// functions.js
function func1(input) {
    console.log("Function 1 (加法) executed with input:", input);
    return input + 2; // 加法：输入值加2
}

function func2(input) {
    console.log("Function 2 (减法) executed with input:", input);
    return input - 1; // 减法：输入值减1
}

function func3(input) {
    console.log("Function 3 (乘法) executed with input:", input);
    return input * 3; // 乘法：输入值乘以3
}
